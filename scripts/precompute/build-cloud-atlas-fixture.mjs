#!/usr/bin/env node
/* global console */
import { mkdir, writeFile } from "node:fs/promises";
import { dirname } from "node:path";
import process from "node:process";

const DEFAULT_OUTPUT = "public/data/fixtures/cloud-atlas.provisional.json";
const WIDTH = 360;
const HEIGHT = 181;
const RESOLUTION_DEG = 1;

const output = readOutputPath(process.argv.slice(2));
const values = [];

for (let y = 0; y < HEIGHT; y += 1) {
  const latitudeDeg = -90 + y * RESOLUTION_DEG;
  const latitudeRad = latitudeDeg * Math.PI / 180;
  for (let x = 0; x < WIDTH; x += 1) {
    const longitudeDeg = -180 + x * RESOLUTION_DEG;
    const longitudeRad = longitudeDeg * Math.PI / 180;
    const equatorialBand = Math.exp(-Math.pow((latitudeDeg - 4 * Math.sin(longitudeRad * 1.4)) / 34, 2));
    const stormTrackNorth = Math.exp(-Math.pow((latitudeDeg - 48 - 6 * Math.sin(longitudeRad * 1.1)) / 13, 2));
    const stormTrackSouth = Math.exp(-Math.pow((latitudeDeg + 46 - 5 * Math.cos(longitudeRad * 1.2)) / 14, 2));
    const planetaryWave =
      0.5 +
      0.5 *
        Math.sin(longitudeRad * 3.1 + Math.sin(latitudeRad * 2.7) * 1.8) *
        Math.cos(longitudeRad * 0.8 - latitudeRad * 1.6);
    const filament =
      0.5 +
      0.5 *
        Math.sin(longitudeRad * 7.7 + latitudeRad * 5.3 + Math.sin(longitudeRad * 2.2) * 0.9);
    const polarSoftening = 1 - Math.min(0.34, Math.abs(latitudeDeg) / 90 * 0.34);
    const cloud01 = clamp01(
      (equatorialBand * 0.42 +
        stormTrackNorth * 0.31 +
        stormTrackSouth * 0.31 +
        planetaryWave * 0.22 +
        filament * 0.12 -
        0.14) *
        polarSoftening,
    );
    values.push(Math.round(cloud01 * 100));
  }
}

const artifact = {
  version: "provisional-fixture-1deg-v1",
  generatedAtUtc: "2026-05-06T00:00:00.000Z",
  validAtUtc: "2026-05-06T00:00:00.000Z",
  resolutionDeg: RESOLUTION_DEG,
  width: WIDTH,
  height: HEIGHT,
  latitudeStartDeg: -90,
  longitudeStartDeg: -180,
  valuesEncoding: "uint8-cloud-cover-pct",
  source: {
    kind: "provisional-fixture",
    model: "deterministic synthetic cloud field",
    provenance:
      "Generated by scripts/precompute/build-cloud-atlas-fixture.mjs. This is not meteorological data; it is a visual pipeline fixture for replacing with NOAA GFS-derived cloud cover.",
  },
  values,
};

await mkdir(dirname(output), { recursive: true });
await writeFile(output, `${JSON.stringify(artifact)}\n`, "utf8");
console.log(`Wrote ${output} (${WIDTH}x${HEIGHT}, ${values.length} values)`);

function readOutputPath(args) {
  const outputIndex = args.indexOf("--output");
  if (outputIndex >= 0) {
    return args[outputIndex + 1] ?? DEFAULT_OUTPUT;
  }
  return DEFAULT_OUTPUT;
}

function clamp01(value) {
  return Math.min(1, Math.max(0, value));
}
