#!/usr/bin/env node
/* global console, fetch, setTimeout, URLSearchParams */
import { mkdir, writeFile } from "node:fs/promises";
import { dirname } from "node:path";
import { fileURLToPath } from "node:url";
import process from "node:process";

const DEFAULT_OUTPUT = "public/data/cloud-atlas.current.json";
const DEFAULT_RESOLUTION_DEG = 1;
const DEFAULT_SOURCE_RESOLUTION_DEG = 10;
const DEFAULT_REQUEST_DELAY_MS = 1000;
const DEFAULT_BATCH_SIZE = 50;
const DEFAULT_MAX_RETRIES = 4;
const DEFAULT_RETRY_DELAY_MS = 15000;
const OPEN_METEO_FORECAST_URL = "https://api.open-meteo.com/v1/forecast";
const CURRENT_VARIABLE = "cloud_cover";

if (isMainModule()) {
  const options = parseArgs(process.argv.slice(2));
  await buildCloudAtlas(options);
}

export async function buildCloudAtlas(options = {}) {
  const resolutionDeg = options.resolutionDeg ?? DEFAULT_RESOLUTION_DEG;
  const sourceResolutionDeg = options.sourceResolutionDeg ?? DEFAULT_SOURCE_RESOLUTION_DEG;
  assertSupportedResolution(resolutionDeg, { maximum: 1, label: "output cloud atlas resolution" });
  assertSupportedResolution(sourceResolutionDeg, { maximum: 30, label: "source cloud atlas resolution" });
  if (sourceResolutionDeg < resolutionDeg) {
    throw new Error("source cloud atlas resolution must be >= output cloud atlas resolution.");
  }

  const output = options.output ?? DEFAULT_OUTPUT;
  const requestDelayMs = options.requestDelayMs ?? DEFAULT_REQUEST_DELAY_MS;
  const batchSize = options.batchSize ?? DEFAULT_BATCH_SIZE;
  const maxRetries = options.maxRetries ?? DEFAULT_MAX_RETRIES;
  const retryDelayMs = options.retryDelayMs ?? DEFAULT_RETRY_DELAY_MS;
  const fetchJson = options.fetchJson ?? fetchJsonFromNetwork;
  const generatedAtUtc = options.generatedAtUtc ?? new Date().toISOString();
  const rows = buildRows(sourceResolutionDeg);
  const sourceWidth = Math.round(360 / sourceResolutionDeg);
  const sourceHeight = rows.length;
  const width = Math.round(360 / resolutionDeg);
  const height = Math.round(180 / resolutionDeg) + 1;
  const sourceValues = [];
  const validTimes = [];

  for (let rowIndex = 0; rowIndex < rows.length; rowIndex += 1) {
    const row = rows[rowIndex];
    const rowValues = [];
    const chunks = chunk(row, batchSize);

    for (let chunkIndex = 0; chunkIndex < chunks.length; chunkIndex += 1) {
      const chunkPoints = chunks[chunkIndex];
      const url = openMeteoCloudCoverUrl(chunkPoints);
      const response = await fetchJsonWithRetry(fetchJson, url, {
        maxRetries,
        retryDelayMs,
        quiet: options.quiet === true,
      });
      const parsed = parseOpenMeteoCloudCoverResponse(response, chunkPoints.length, url);
      rowValues.push(...parsed.values);
      validTimes.push(...parsed.validTimes);

      const isLastRequest = rowIndex === rows.length - 1 && chunkIndex === chunks.length - 1;
      if (!isLastRequest && requestDelayMs > 0) {
        await delay(requestDelayMs);
      }
    }

    if (rowValues.length !== sourceWidth) {
      throw new Error(
        `Open-Meteo row ${rowIndex} yielded ${rowValues.length} values; expected ${sourceWidth}.`,
      );
    }
    sourceValues.push(...rowValues);
    if (options.quiet !== true) {
      console.log(
        `cloud atlas source row ${rowIndex + 1}/${sourceHeight} lat ${row[0]?.latitudeDeg.toFixed(2)} done`,
      );
    }
  }

  const values = resampleCloudAtlasValues({
    sourceValues,
    sourceResolutionDeg,
    sourceWidth,
    sourceHeight,
    outputResolutionDeg: resolutionDeg,
    outputWidth: width,
    outputHeight: height,
  });
  const validAtUtc = mostCommonValidTime(validTimes) ?? generatedAtUtc;
  const artifact = {
    version: `open-meteo-cloud-atlas-${resolutionDeg}deg-from-${sourceResolutionDeg}deg-v1`,
    generatedAtUtc,
    validAtUtc,
    resolutionDeg,
    width,
    height,
    latitudeStartDeg: -90,
    longitudeStartDeg: -180,
    valuesEncoding: "uint8-cloud-cover-pct",
    source: {
      kind: "open-meteo",
      model: "Open-Meteo Forecast API current cloud_cover",
      provenance:
        "Generated by scripts/precompute/build-cloud-atlas-openmeteo.mjs from Open-Meteo batch current cloud_cover requests. Source points are bilinearly resampled into the output atlas. This artifact is visual-only and is retained as a legacy bridge.",
      requestCount: Math.ceil(sourceWidth / batchSize) * sourceHeight,
      batchSize,
      sourceResolutionDeg,
      interpolation: sourceResolutionDeg === resolutionDeg ? "none" : "bilinear-from-source-grid",
    },
    values,
  };

  if (values.length !== width * height) {
    throw new Error(`cloud atlas yielded ${values.length} values; expected ${width * height}.`);
  }

  await mkdir(dirname(output), { recursive: true });
  await writeFile(output, `${JSON.stringify(artifact)}\n`, "utf8");
  if (options.quiet !== true) {
    console.log(`Wrote ${output} (${width}x${height}, ${values.length} values, valid ${validAtUtc})`);
  }
  return artifact;
}

export function openMeteoCloudCoverUrl(points) {
  if (points.length === 0) {
    throw new Error("Open-Meteo cloud atlas request requires at least one point.");
  }

  const params = new URLSearchParams({
    latitude: points.map((point) => formatCoordinate(point.latitudeDeg)).join(","),
    longitude: points.map((point) => formatCoordinate(point.longitudeDeg)).join(","),
    current: CURRENT_VARIABLE,
    timezone: "UTC",
  });
  return `${OPEN_METEO_FORECAST_URL}?${params.toString()}`;
}

export function parseOpenMeteoCloudCoverResponse(response, expectedCount, source) {
  const items = Array.isArray(response) ? response : [response];
  if (items.length !== expectedCount) {
    throw new Error(`${source} returned ${items.length} locations; expected ${expectedCount}.`);
  }

  const values = [];
  const validTimes = [];
  for (const [index, item] of items.entries()) {
    if (typeof item !== "object" || item === null || !("current" in item)) {
      throw new Error(`${source} location ${index} is missing current cloud_cover.`);
    }
    const current = item.current;
    if (typeof current !== "object" || current === null) {
      throw new Error(`${source} location ${index} current payload is invalid.`);
    }
    const cloudCover = current.cloud_cover;
    if (typeof cloudCover !== "number" || !Number.isFinite(cloudCover)) {
      throw new Error(`${source} location ${index} cloud_cover must be numeric.`);
    }
    if (typeof current.time === "string") {
      validTimes.push(normalizeOpenMeteoTime(current.time));
    }
    values.push(clampUint8(Math.round(cloudCover)));
  }

  return { values, validTimes };
}

export function buildRows(resolutionDeg) {
  assertSupportedResolution(resolutionDeg, { maximum: 30, label: "cloud atlas row resolution" });
  const width = Math.round(360 / resolutionDeg);
  const height = Math.round(180 / resolutionDeg) + 1;
  const rows = [];

  for (let y = 0; y < height; y += 1) {
    const latitudeDeg = -90 + y * resolutionDeg;
    const row = [];
    for (let x = 0; x < width; x += 1) {
      row.push({
        latitudeDeg,
        longitudeDeg: -180 + x * resolutionDeg,
      });
    }
    rows.push(row);
  }

  return rows;
}

function parseArgs(args) {
  const options = {};
  for (let index = 0; index < args.length; index += 1) {
    const arg = args[index];
    switch (arg) {
      case "--output":
        options.output = requireValue(args, index, arg);
        index += 1;
        break;
      case "--step":
      case "--resolution-deg":
        options.resolutionDeg = Number(requireValue(args, index, arg));
        index += 1;
        break;
      case "--source-step":
      case "--source-resolution-deg":
        options.sourceResolutionDeg = Number(requireValue(args, index, arg));
        index += 1;
        break;
      case "--request-delay-ms":
        options.requestDelayMs = Number(requireValue(args, index, arg));
        index += 1;
        break;
      case "--batch-size":
        options.batchSize = Number(requireValue(args, index, arg));
        index += 1;
        break;
      case "--max-retries":
        options.maxRetries = Number(requireValue(args, index, arg));
        index += 1;
        break;
      case "--retry-delay-ms":
        options.retryDelayMs = Number(requireValue(args, index, arg));
        index += 1;
        break;
      case "--quiet":
        options.quiet = true;
        break;
      case "--help":
        printHelp();
        process.exit(0);
        break;
      default:
        throw new Error(`Unknown option ${arg}`);
    }
  }

  return options;
}

function printHelp() {
  console.log(`Usage:
  node scripts/precompute/build-cloud-atlas-openmeteo.mjs [options]

Options:
  --output <path>             Output artifact path. Default: ${DEFAULT_OUTPUT}
  --step <deg>                Grid resolution in degrees. Default: ${DEFAULT_RESOLUTION_DEG}
  --source-step <deg>         Open-Meteo source anchor grid. Default: ${DEFAULT_SOURCE_RESOLUTION_DEG}
  --request-delay-ms <ms>     Delay between API requests. Default: ${DEFAULT_REQUEST_DELAY_MS}
  --batch-size <count>        Locations per API request. Default: ${DEFAULT_BATCH_SIZE}
  --max-retries <count>       Retry count for 429 / 5xx. Default: ${DEFAULT_MAX_RETRIES}
  --retry-delay-ms <ms>       Base retry delay. Default: ${DEFAULT_RETRY_DELAY_MS}
  --quiet                     Suppress row progress logs
`);
}

function requireValue(args, index, optionName) {
  const value = args[index + 1];
  if (value === undefined || value.startsWith("--")) {
    throw new Error(`${optionName} requires a value.`);
  }
  return value;
}

async function fetchJsonFromNetwork(url) {
  const response = await fetch(url, {
    headers: {
      "User-Agent": "PENUMBRA cloud-atlas precompute",
    },
  });
  if (!response.ok) {
    const error = new Error(`Failed to fetch ${url}: ${response.status} ${response.statusText}`);
    error.status = response.status;
    throw error;
  }
  return response.json();
}

async function fetchJsonWithRetry(fetchJson, url, options) {
  let attempt = 0;
  for (;;) {
    try {
      return await fetchJson(url);
    } catch (error) {
      if (attempt >= options.maxRetries || !isRetryableFetchError(error)) {
        throw error;
      }
      attempt += 1;
      const delayMs = options.retryDelayMs * attempt;
      if (!options.quiet) {
        console.warn(`cloud atlas request retry ${attempt}/${options.maxRetries} in ${delayMs}ms`);
      }
      await delay(delayMs);
    }
  }
}

function isRetryableFetchError(error) {
  const status = typeof error === "object" && error !== null && "status" in error ? error.status : undefined;
  if (status === 429) {
    return true;
  }
  return typeof status === "number" && status >= 500 && status < 600;
}

export function resampleCloudAtlasValues({
  sourceValues,
  sourceResolutionDeg,
  sourceWidth,
  sourceHeight,
  outputResolutionDeg,
  outputWidth,
  outputHeight,
}) {
  const outputValues = [];
  for (let y = 0; y < outputHeight; y += 1) {
    const latitudeDeg = -90 + y * outputResolutionDeg;
    for (let x = 0; x < outputWidth; x += 1) {
      const longitudeDeg = -180 + x * outputResolutionDeg;
      outputValues.push(
        clampUint8(
          Math.round(
            sampleSourceCloudAtlas(
              sourceValues,
              sourceWidth,
              sourceHeight,
              sourceResolutionDeg,
              latitudeDeg,
              longitudeDeg,
            ),
          ),
        ),
      );
    }
  }
  return outputValues;
}

function sampleSourceCloudAtlas(values, width, height, resolutionDeg, latitudeDeg, longitudeDeg) {
  const yFloat = (Math.min(90, Math.max(-90, latitudeDeg)) + 90) / resolutionDeg;
  const xFloat = wrapDegrees360(longitudeDeg + 180) / resolutionDeg;
  const y0 = clampIndex(Math.floor(yFloat), height);
  const y1 = clampIndex(y0 + 1, height);
  const x0 = wrapIndex(Math.floor(xFloat), width);
  const x1 = wrapIndex(x0 + 1, width);
  const tx = xFloat - Math.floor(xFloat);
  const ty = yFloat - Math.floor(yFloat);
  const top = lerp(valueAt(values, width, x0, y0), valueAt(values, width, x1, y0), tx);
  const bottom = lerp(valueAt(values, width, x0, y1), valueAt(values, width, x1, y1), tx);
  return lerp(top, bottom, ty);
}

function valueAt(values, width, x, y) {
  return values[y * width + x] ?? 0;
}

function lerp(left, right, mix01) {
  return left + (right - left) * mix01;
}

function wrapIndex(index, length) {
  return ((index % length) + length) % length;
}

function wrapDegrees360(value) {
  return ((value % 360) + 360) % 360;
}

function clampIndex(index, length) {
  return Math.max(0, Math.min(length - 1, index));
}

function assertSupportedResolution(resolutionDeg, options) {
  if (!Number.isFinite(resolutionDeg) || resolutionDeg <= 0 || resolutionDeg > options.maximum) {
    throw new Error(`${options.label} must be > 0 and <= ${options.maximum} degrees.`);
  }
  const longitudeSteps = 360 / resolutionDeg;
  const latitudeSteps = 180 / resolutionDeg;
  if (!Number.isInteger(longitudeSteps) || !Number.isInteger(latitudeSteps)) {
    throw new Error(`${options.label} must divide both 180 and 360 exactly.`);
  }
}

function chunk(values, size) {
  const chunkSize = Math.max(1, Math.floor(size));
  const chunks = [];
  for (let index = 0; index < values.length; index += chunkSize) {
    chunks.push(values.slice(index, index + chunkSize));
  }
  return chunks;
}

function mostCommonValidTime(times) {
  const counts = new Map();
  for (const time of times) {
    counts.set(time, (counts.get(time) ?? 0) + 1);
  }

  let bestTime;
  let bestCount = 0;
  for (const [time, count] of counts.entries()) {
    if (count > bestCount) {
      bestTime = time;
      bestCount = count;
    }
  }

  return bestTime;
}

function normalizeOpenMeteoTime(time) {
  return time.endsWith("Z") ? time : `${time}:00.000Z`;
}

function formatCoordinate(value) {
  return Number.isInteger(value) ? String(value) : value.toFixed(4).replace(/0+$/, "").replace(/\.$/, "");
}

function clampUint8(value) {
  return Math.min(100, Math.max(0, value));
}

function delay(ms) {
  return new Promise((resolve) => {
    setTimeout(resolve, ms);
  });
}

function isMainModule() {
  return process.argv[1] !== undefined && fileURLToPath(import.meta.url) === process.argv[1];
}
